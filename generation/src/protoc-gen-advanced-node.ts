// noinspection ExceptionCaughtLocallyJS

import {
	createEcmaScriptPlugin,
	runNodeJs,
	type Schema,
} from '@bufbuild/protoplugin';
import type { DescFile, DescService, } from "@bufbuild/protobuf"
import generateTriggerFiles from './nodes/generateTriggerFiles';
import generateActionFiles from './nodes/generateActionFiles';

// noinspection JSUnusedGlobalSymbols
const plugin = createEcmaScriptPlugin({
	name: 'protoc-gen-advanced-node',
	version: 'v1',
	generateTs(schema: Schema) {
		try {
			const preamble = `// @generated by ${this.name} ${this.version}`;
			const actionServices: DescService[] = schema.files.flatMap((f: DescFile) => f.services).filter(s => s.name.endsWith('CommandService'));
			const triggerServices: DescService[] = schema.files.flatMap(f => f.services).filter(s => s.name.endsWith('NotificationService'));

			// check if there is an unexpected service type
			const unknownServices = schema.files.flatMap(f => f.services).filter(s => !s.name.endsWith('CommandService') && !s.name.endsWith('NotificationService') && !s.name.endsWith('AdminService'));
			if (unknownServices.length > 0) {
				throw new Error(`#--# GENERATION ERROR: Unrecognized service type for ${unknownServices}`);
			}

			// generateAction actions
			if (actionServices.length > 0) {
				generateActionFiles(schema, actionServices, preamble, false);
			}

			// generateAction triggers
			if (triggerServices.length > 0) {
				generateTriggerFiles(schema, triggerServices, preamble, false);
			}
		}
		catch (e) {
			console.error(e);
			throw e
		}
	},
});

// Reads the schema from stdin, runs the plugin, and writes the generated nodes to stdout.
runNodeJs(plugin);
