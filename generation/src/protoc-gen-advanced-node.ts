import {
	createEcmaScriptPlugin,
	runNodeJs,
	type Schema,
} from '@bufbuild/protoplugin';
import { generateRouterCallOperation } from './nodes/actions/generateRouterCallOperation';
import type { ResourceOperationListType } from './tools/types';
import { generateActionInterfaces } from './nodes/actions/generateActionInterfaces';
import { generateActionVersionDescription } from './nodes/actions/generateActionVersionDescription';
import { generateAction } from './nodes/actions/generateAction';
import { generateActionIndexes } from './nodes/actions/generateActionIndexes';
import { listFilesInDirectory } from './tools/files';
import * as path from 'path';
import { overrideFile } from './nodes/overrideFile';
import { generateTrigger } from './nodes/triggers/generateTrigger';
import { generateTriggerProperties } from './nodes/triggers/generateTriggerProperties';
import { generateTriggerRouterMap } from './nodes/triggers/generateTriggerRouterMap';
import type { DescMethod, DescService } from '@bufbuild/protobuf';

// noinspection JSUnusedGlobalSymbols
const plugin = createEcmaScriptPlugin({
	name: 'protoc-gen-advanced-node',
	version: 'v1',
	generateTs(schema: Schema) {
		try {
			const preamble = `// @generated by ${this.name} ${this.version} with parameter "target=ts"`;
			const actionResourceOperationsList: ResourceOperationListType = {};
			const triggerServices: DescService[] = [];
			const triggerMethods: DescMethod[] = [];
			const overrideFileList: string[] = listFilesInDirectory(path.join(__dirname, "nodes/overrides"))

			// browse services
			for (const file of schema.files) {
				for (const service of file.services) {

					if (service.name.endsWith("NotificationService")) {
						// @ts-ignore
						triggerServices.push(service);
					}

					for (const method of service.methods) {
						// ignore admin services
						if (service.name.endsWith('AdminService')) { }
						// notification services
						else if (service.name.endsWith('NotificationService')) {
							// @ts-ignore
							triggerMethods.push(method);
							const isOverwritten = overrideFile(schema, overrideFileList, method, "triggers");
							if (!isOverwritten)
								generateTrigger(schema, method, false);
						}
						// command services
						else if (service.name.endsWith('CommandService')) {
							const isOverwritten = overrideFile(schema, overrideFileList, method, "actions");
							if (!actionResourceOperationsList[service.name])
								actionResourceOperationsList[service.name] = [];
							actionResourceOperationsList[service.name].push(method.name);
							if (!isOverwritten)
								generateAction(schema, method);
						}
						else {
							throw new Error(`#--# GENERATION ERROR: Unrecognized service type for ${service.name}`);
						}
					}
				}
			}
			if (Object.keys(actionResourceOperationsList).length !== 0) {
				// Action Indexes
				for (const resource in actionResourceOperationsList) {
					const actionIndexFile = schema.generateFile(`actions/${resource}/index.ts`);
					actionIndexFile.print(preamble);
					generateActionIndexes(actionIndexFile, actionResourceOperationsList, resource);
				}
				// Action Interfaces
				const interfacesFile = schema.generateFile('actions/generatedInterfaces.ts');
				interfacesFile.print(preamble);
				generateActionInterfaces(interfacesFile, actionResourceOperationsList);
				// Router Call Operation
				const callOperationfile = schema.generateFile('actions/generatedCallOperation.ts');
				callOperationfile.print(preamble);
				generateRouterCallOperation(callOperationfile, actionResourceOperationsList);
				// Action Version Description
				const versionDescriptionFile = schema.generateFile('actions/generatedVersionDescription.ts');
				versionDescriptionFile.print(preamble);
				generateActionVersionDescription(versionDescriptionFile, actionResourceOperationsList);
			}

			if (triggerServices.length !== 0) {
				// generate trigger structure nodes
				// Trigger Resource Description
				const triggerResourceDescriptionFile = schema.generateFile('triggers/generatedTriggerProperties.ts');
				triggerResourceDescriptionFile.print(preamble);
				generateTriggerProperties(triggerResourceDescriptionFile, triggerServices);

				// Trigger Router Map
				const routerMapFile = schema.generateFile('triggers/triggerMap.ts');
				routerMapFile.print(preamble);
				generateTriggerRouterMap(routerMapFile, triggerMethods);
			}
		}
		catch (e) {
			console.error(e);
			throw e
		}
	},
});

// Reads the schema from stdin, runs the plugin, and writes the generated nodes to stdout.
runNodeJs(plugin);
